#ifndef ALPHABET_H
#define ALPHABET_H

/**
 * @brief The Alphabet enum enumeration used to represent the first coordinate of
 * the ABA-BRO coordinates sytem.
 * These values are used  in arguments to various methods in the model.
 */
enum Alphabet {
    A = -4,
    B = -3,
    C = -2,
    D = -1,
    E = 0,
    F = 1,
    G = 2,
    H = 3,
    I = 4
 };

#endif // ALPHABET_H
